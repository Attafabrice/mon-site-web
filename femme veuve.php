                    <h2 class="about-title">A savoir</h2>
                    <p class="about-text">
                        Les droits des femmes veuves sont protégés par la loi internationale, notamment par la Convention sur l'élimination de toutes les formes de discrimination à l'égard des femmes (CEDAW). La CEDAW garantit aux femmes veuves le droit à l'égalité, à la non-discrimination, à la protection contre la violence et à l'accès aux ressources économiques.
                    </p>
                    <p class="about-text">
                    </p>
                    <p class="about-text">
                        Cependant, les femmes veuves en Afrique sont souvent confrontées à des discriminations et des violations de leurs droits. Elles peuvent être spoliées de leur héritage, chassées de leur domicile, forcées de se remarier ou victimes de violence.
                    </p>
